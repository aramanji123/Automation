/**
Normal arithmetic operators:
+(addition)
-(subtraction)
*(multipication)
/(Divison)
%(reminder)
**(power)
*/

//addition (+)

def a=3+1
assert a==4
println "a:$a"

//subtraction(-)
a=4-1
assert a==3
println "a:$a"

//multipication(*)
a=3*4
assert a==12
println "a:$a"

//Division(/)
a=12/4
assert a==3
println "a:$a"

//reminder or modulation (%)
a=3%3
assert a==0
println "a:$a"

//power (**)
a=2**2
assert a==4
println "a:$a"
